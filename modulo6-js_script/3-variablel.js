//CONST cria constante, não podem ser alterada
const constante = 10;
console.log(constante);

//LET cria variáveis MULTÁVEIS.
let variavel_let = 10;
console.log(constante);
variavel_let = 39;
console.log(variavel_let)

//VAR igual o LET, cria variáveis MULTÁVEIS
var variavel_var = 10;
console.log(constante);
variavel_var = 39;
console.log(variavel_let)

//VAR e LET podem ser inicializada sem valor, mas CONST não pode.
